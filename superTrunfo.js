function cardImage (card, icon){
    icon.style.backgroundImage = `url('./Images/Pokemon-jpg/${card.id}.jpg')`
    icon.style.backgroundSize = "300px 225px"
    icon.style.backgroundPosition = "25px 45px"
    //icon.style.backgroundColor = "ff7f7f"

    var moldura = `<img src="./Images/template.png"
    style=" width: inherit; height: inherit; position: absolute;">`;

    var tagHTML = `<div id='opcoes' class='carta-status'>`

    var opcoesHtml = ""
    for (var atributo in card.atributos){
         opcoesHtml += `<input type="radio" name="atributos" onClick=selecionarAtributo() value="${atributo}">${atributo}: ${card.atributos[atributo]}<br>`
    }

    var nome = `<p class="carta-subtitle">${card.nome}</p><br>`
    var tipo = `<p class="carta-type">${card.tipo}</p>`

    icon.innerHTML = moldura + tagHTML + nome + tipo + opcoesHtml +"</div>" + "</div>"
}

function selecionarAtributo (){
    var opcoesAtributo = document.getElementsByName("atributos");

    for (var i=0; i<opcoesAtributo.length; i++){
        
        if (opcoesAtributo[i].checked==true){
            document.getElementById("btnJogar").disabled = false;
            return opcoesAtributo[i].value
        }
    }
}

